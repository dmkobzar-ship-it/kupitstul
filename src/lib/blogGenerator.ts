export function getTodayArticle(): BlogArticle {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) /
      (1000 * 60 * 60 * 24),
  );
  const idx = dayOfYear % ARTICLE_POOL.length;
  const template = ARTICLE_POOL[idx];

  const dateStr = today.toLocaleDateString("ru-RU", {
    day: "numeric",
    month: "long",
    year: "numeric",
  });

  return {
    id: `auto-${today.getFullYear()}-${dayOfYear}`,
    date: dateStr,
    ...template,
  };
}

export function getWeekArticles(): BlogArticle[] {
  const today = new Date();
  const articles: BlogArticle[] = [];

  for (let offset = 0; offset < 7; offset++) {
    const d = new Date(today);
    d.setDate(d.getDate() - offset);
    const dayOfYear = Math.floor(
      (d.getTime() - new Date(d.getFullYear(), 0, 0).getTime()) /
        (1000 * 60 * 60 * 24),
    );
    const idx = dayOfYear % ARTICLE_POOL.length;
    const template = ARTICLE_POOL[idx];
    const dateStr = d.toLocaleDateString("ru-RU", {
      day: "numeric",
      month: "long",
      year: "numeric",
    });

    articles.push({
      id: `auto-${d.getFullYear()}-${dayOfYear}`,
      date: dateStr,
      ...template,
    });
  }

  return articles;
}
